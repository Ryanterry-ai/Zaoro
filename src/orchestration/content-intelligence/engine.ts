// ─── Helpers ──────────────────────────────────────────────────────────────────

function makeProvenance(layer: Provenance['layer'], confidence: number, evidence: string[] = []): Provenance {
  return {
    layer,
    confidence,
    evidence,
    timestamp: new Date(),
    reasoning: `Generated by ${layer}`,
    source: 'content-intelligence',
  };
}

