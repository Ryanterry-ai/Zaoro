// ─── FastAPI Middleware ───────────────────────────────────────────────────────

function generateFastAPIMiddleware(config: AuthConfig): string {
  const csrfBlock = config.csrfEnabled !== false
    ? [
        '',
        '# \u2500\u2500\u2500 CSRF Protection \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500',
        '',
        'def create_csrf_token(session_id: str) -> str:',
        '    timestamp = str(int(datetime.now(timezone.utc).timestamp()))',
        '    payload = f"{session_id}:{timestamp}"',
        '    signature = hmac.new(CSRF_SECRET.encode(), payload.encode(), hashlib.sha256).hexdigest()',
        '    return f"{payload}:{signature}"',
        '',
        'def verify_csrf_token(token: str, session_id: str) -> bool:',
        '    parts = token.split(":")',
        '    if len(parts) != 3:',
        '        return False',
        '    payload_ts = f"{session_id}:{parts[1]}"',
        '    expected = hmac.new(CSRF_SECRET.encode(), payload_ts.encode(), hashlib.sha256).hexdigest()',
        '    return hmac.compare_digest(expected, parts[2])',
        '',
        'async def csrf_protect(request: Request):',
        '    if request.method in ("GET", "HEAD", "OPTIONS"):',
        '        return',
        '    if request.url.path.startswith("/api/"):',
        '        token = request.headers.get("X-CSRF-Token") or request.cookies.get("csrf_token")',
        '        session_id = request.cookies.get("session_id", "")',
        '        if not token or not verify_csrf_token(token, session_id):',
        '            raise HTTPException(status_code=status.HTTP_403_FORBIDDEN, detail="Invalid CSRF token.")',
        '',
      ].join('\n')
    : '';

  return [
    '"""',
    'Auth Middleware \u2014 JWT verification, role-based access control, CSRF protection.',
    '"""',
    'from __future__ import annotations',
    '',
    'import hashlib',
    'import hmac',
    'import os',
    'import secrets',
    'from datetime import datetime, timezone',
    'from typing import Annotated',
    '',
    'from fastapi import Cookie, Depends, HTTPException, Request, status',
    'from fastapi.security import HTTPAuthorizationCredentials, HTTPBearer',
    'from jose import JWTError, jwt',
    '',
    'security = HTTPBearer(auto_error=False)',
    '',
    'SECRET_KEY = os.environ.get("' + config.secretKey + '", "CHANGE-ME-IN-PRODUCTION")',
    'ALGORITHM = "HS256"',
    'CSRF_SECRET = os.environ.get("CSRF_SECRET", os.urandom(32).hex())',
    '',
    csrfBlock,
    '# \u2500\u2500\u2500 Token Verification \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500',
    '',
    'def decode_token(token: str) -> dict:',
    '    try:',
    '        payload = jwt.decode(token, SECRET_KEY, algorithms=[ALGORITHM])',
    '        exp = payload.get("exp")',
    '        if exp and datetime.fromtimestamp(exp, tz=timezone.utc) < datetime.now(timezone.utc):',
    '            raise HTTPException(',
    '                status_code=status.HTTP_401_UNAUTHORIZED,',
    '                detail="Token has expired.",',
    '                headers={"WWW-Authenticate": "Bearer"},',
    '            )',
    '        return payload',
    '    except JWTError:',
    '        raise HTTPException(',
    '            status_code=status.HTTP_401_UNAUTHORIZED,',
    '            detail="Invalid token.",',
    '            headers={"WWW-Authenticate": "Bearer"},',
    '        )',
    '',
    '# \u2500\u2500\u2500 Current User Dependency \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500',
    '',
    'async def get_current_user(',
    '    request: Request,',
    '    credentials: Annotated[HTTPAuthorizationCredentials | None, Depends(security)] = None,',
    '):',
    '    token: str | None = None',
    '',
    '    if credentials:',
    '        token = credentials.credentials',
    '    else:',
    '        token = request.cookies.get("access_token")',
    '',
    '    if not token:',
    '        raise HTTPException(',
    '            status_code=status.HTTP_401_UNAUTHORIZED,',
    '            detail="Not authenticated.",',
    '            headers={"WWW-Authenticate": "Bearer"},',
    '        )',
    '',
    '    payload = decode_token(token)',
    '    user_id = payload.get("sub")',
    '    if not user_id:',
    '        raise HTTPException(status_code=status.HTTP_401_UNAUTHORIZED, detail="Invalid token payload.")',
    '',
    '    request.state.user_id = user_id',
    '    request.state.user_role = payload.get("role", "user")',
    '    return {"id": user_id, "role": payload.get("role", "user")}',
    '',
    '# \u2500\u2500\u2500 Role-Based Access Control \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500',
    '',
    'def require_roles(*allowed_roles: str):',
    '    async def role_checker(',
    '        current_user: Annotated[dict, Depends(get_current_user)],',
    '    ):',
    '        user_role = current_user.get("role", "")',
    '        if user_role not in allowed_roles:',
    '            raise HTTPException(',
    '                status_code=status.HTTP_403_FORBIDDEN,',
    '                detail=f"Role \'{user_role}\' is not authorized. Required: {\', \'.join(allowed_roles)}",',
    '            )',
    '        return current_user',
    '    return Depends(role_checker)',
    '',
    '# \u2500\u2500\u2500 Convenience Defaults \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500',
    '',
    'require_admin = require_roles("admin")',
    'require_user = require_roles("user", "admin")',
    '',
  ].join('\n');
}

