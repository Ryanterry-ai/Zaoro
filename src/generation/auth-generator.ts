// ─── Types ────────────────────────────────────────────────────────────────────

export interface AuthConfig {
  provider: 'jwt' | 'session';
  passwordHashing: 'bcrypt' | 'argon2';
  tokenExpiry: string;
  refreshExpiry: string;
  secretKey: string;
  roles?: string[];
  rateLimitWindow?: string;
  rateLimitMax?: number;
  cookieDomain?: string;
  csrfEnabled?: boolean;
}

export interface AuthCode {
  fastapi: {
    authRouter: string;
    middleware: string;
    models: string;
    utils: string;
  };
  nextjs: {
    authApi: string;
    middleware: string;
    authHook: string;
    loginPage: string;
    registerPage: string;
  };
  config: string;
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

function rolesList(config: AuthConfig): string[] {
  return config.roles ?? ['admin', 'user'];
}

function tokenExpiryPy(config: AuthConfig): string {
  if (config.tokenExpiry.includes('m')) {
    return 'timedelta(minutes=' + config.tokenExpiry.replace('m', '').trim() + ')';
  }
  return 'timedelta(hours=' + config.tokenExpiry.replace('h', '').trim() + ')';
}

function refreshExpiryPy(config: AuthConfig): string {
  if (config.refreshExpiry.includes('d')) {
    return 'timedelta(days=' + config.refreshExpiry.replace('d', '').trim() + ')';
  }
  return 'timedelta(hours=' + config.refreshExpiry.replace('h', '').trim() + ')';
}

function rateWindowPy(config: AuthConfig): string {
  if (!config.rateLimitWindow) return '60.0';
  if (config.rateLimitWindow.includes('m')) {
    return config.rateLimitWindow.replace('m', '').trim() + '.0 * 60';
  }
  return config.rateLimitWindow.replace('h', '').trim() + '.0 * 3600';
}

function rateWindowMs(config: AuthConfig): string {
  if (!config.rateLimitWindow) return '60 * 1000';
  if (config.rateLimitWindow.includes('m')) {
    return config.rateLimitWindow.replace('m', '').trim() + ' * 60 * 1000';
  }
  return config.rateLimitWindow.replace('h', '').trim() + ' * 3600 * 1000';
}

function refreshMaxAge(config: AuthConfig): string {
  if (config.refreshExpiry.includes('d')) {
    return "parseInt('" + config.refreshExpiry.replace('d', '').trim() + "') * 86400";
  }
  return '7 * 86400';
}

